#include "libft.h"

char **ft_cut_last_tab(char **tab)
{
	int i;

	i = 0;
	while (tab[i])
	{
		if (tab[i + 1] == NULL)
			tab[i] = NULL;
		i++;
	}
	return (tab);
}
