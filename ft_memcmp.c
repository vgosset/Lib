#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*ptr;
	unsigned char	*ptr2;

	i = 0;
	ptr = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	while (i < n && ptr[i] == ptr2[i])
		i++;
	if (i == n)
		return (0);
	return (ptr[i] - ptr2[i]);
}
