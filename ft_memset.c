#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*ptr;

	ptr = b;
	i = 0;
	while (i < (int)len)
	{
		ptr[i] = c;
		i++;
	}
	return (ptr);
}
