#include "ft_printf.h"

long	ft_power(long nb, int i)
{
	long	res;

	res = 1;
	if (i < 0)
		return (-1);
	else if (i == 0)
		return (1);
	else
		while (i > 0)
		{
			res *= nb;
			i--;
		}
	return (res);
}
