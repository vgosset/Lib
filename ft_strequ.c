#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int res;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	res = s1[i] - s2[i];
	if (res != 0)
		return (0);
	else
		return (1);
}
