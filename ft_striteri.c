void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int				i;
	unsigned int	j;

	j = 0;
	i = 0;
	if (!s || !f)
		return ;
	while (s[i])
	{
		(*f)(j, s + i);
		i++;
		j++;
	}
}
